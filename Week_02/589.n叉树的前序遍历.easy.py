"""
# Definition for a Node.
class Node(object):
    def __init__(self, val=None, children=None):
        self.val = val
        self.children = children
"""

class Solution(object):
    def preorder(self, root):
        """
        :type root: Node
        :rtype: List[int]
        """
        if root is None:
            return []
        
        stack, res = [root], []            

        while stack:
            root = stack.pop()
            res.append(root.val)
            stack.extend(root.children[::-1])
                
        return res

